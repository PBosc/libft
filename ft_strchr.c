#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] && s[i] != c % 256)
		i++;
	if (s[i] == c % 256)
		return ((char *)(s + i));
	if (c == 0)
		return ((char *)s);
	return (NULL);
}
