#include <unistd.h>

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*tmp;

	tmp = (unsigned char *) s;
	while (n)
	{
		tmp[n - 1] = 0;
		n--;
	}
	return (s);
}
